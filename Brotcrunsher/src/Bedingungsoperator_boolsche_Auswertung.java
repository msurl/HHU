 
public class Bedingungsoperator_boolsche_Auswertung {
	public static void main(String args[]){
		 int x = 3;
		 int y = 4;
		 int z = x<y?x:y; 
		 /*x<y? entspricht einer boolschen Auswertung; hier finde kleinern Wert
		  * Def. Bedingungsoperator
		  */
		 System.out.println(z);
		 
	}

}
